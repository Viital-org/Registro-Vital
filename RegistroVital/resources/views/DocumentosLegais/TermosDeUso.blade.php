<h2>Termos de Uso do Aplicativo "Registro Vital"</h2>

<h3>1. Aceitação dos Termos</h3>
<p>
    Ao utilizar o aplicativo "Registro Vital", você concorda com os presentes Termos de Uso. Caso descumpra estes termos, seu acesso ao aplicativo poderá ser suspenso ou encerrado.
</p>

<h3>2. Objetivo do Aplicativo</h3>
<p>
    O "Registro Vital" é uma plataforma para que pacientes registrem anotações diárias sobre sua saúde e compartilhem, quando necessário, com profissionais de saúde. Profissionais também podem fornecer recomendações de saúde aos pacientes, respeitando as normas de privacidade e a veracidade das informações.
</p>

<h3>3. Cadastro e Veracidade das Informações</h3>
<p>
    Todos os usuários são responsáveis por fornecer informações verdadeiras tanto no cadastro quanto no uso do aplicativo. Informações falsas podem resultar em consequências legais e no encerramento da conta.
</p>

<h4>3.1 Pacientes</h4>
<p>
    Pacientes podem registrar informações diárias e definir metas pessoais de saúde. Cada anotação pode ser marcada como "Oculta" (visível apenas para o paciente) ou "Visível" (disponível para profissionais durante os agendamentos).
</p>

<h4>3.2 Profissionais de Saúde</h4>
<p>
    Profissionais devem submeter um cadastro com dados de qualificação, que será analisado por um administrador antes da aprovação para uso. O registro deve incluir área de atuação e especializações. Todos os dados fornecidos devem ser verdadeiros e comprováveis.
</p>

<h3>4. Anotações e Visibilidade</h3>
<p>
    Ao registrar uma anotação, o paciente pode optar por deixá-la visível ou oculta. Anotações visíveis ficam disponíveis para os profissionais durante os agendamentos e apenas depois de a consulta ser confirmada pelo paciente.
</p>

<h3>5. Agendamento de Consultas</h3>
<p>
    O sistema permite que pacientes agendem consultas com profissionais, escolhendo a área de especialização e o horário desejado. O profissional pode aceitar ou recusar o agendamento. Detalhes da consulta não serão armazenados, mas o profissional pode fazer recomendações ao paciente via texto ou imagem.
</p>

<h3>6. Conduta e Responsabilidades</h3>

<h4>6.1 Pacientes</h4>
<ul>
    <li><strong>Veracidade</strong>: O paciente deve registrar informações corretas e verdadeiras.</li>
    <li><strong>Definição de metas</strong>: As metas definidas são compromissos pessoais do paciente.</li>
</ul>

<h4>6.2 Profissionais de Saúde</h4>
<ul>
    <li><strong>Confidencialidade</strong>: Profissionais devem proteger a privacidade dos pacientes e usar as informações visíveis apenas para acompanhamento e recomendações.</li>
    <li><strong>Recomendações de saúde</strong>: As recomendações aos pacientes devem ser feitas de forma ética e responsável, respeitando as normas de confidencialidade.</li>
</ul>

<h3>7. Responsabilidades do Administrador</h3>
<p>
    O administrador revisará os cadastros de profissionais e poderá aprovar ou recusar com base na documentação e veracidade das informações fornecidas.
</p>

<h3>8. Privacidade e Proteção de Dados</h3>
<p>
    Todos os dados do "Registro Vital" são tratados de acordo com as leis de proteção de dados. Nenhuma informação sensível será compartilhada com terceiros sem autorização do usuário.
</p>

<h3>9. Alterações nos Termos de Uso</h3>
<p>
    O "Registro Vital" pode alterar estes Termos de Uso a qualquer momento. Notificaremos os usuários sobre alterações importantes, e a continuidade no uso do aplicativo implica aceitação dos novos termos.
</p>
